    private void btnInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarActionPerformed
        // TODO add your handling code here:
        int num = Integer.valueOf(txtNum.getText());
        int res = b.insertarNum(num);
        if (res == -1){
            JOptionPane.showMessageDialog(null, "Numero Repetido");
        }else{
            JOptionPane.showMessageDialog(null, "Numero Insertado");
        }
        txtNum.setText("");
    }//GEN-LAST:event_btnInsertarActionPerformed

    private void btnRecorrerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRecorrerActionPerformed
        // TODO add your handling code here:
        String[] inOrden = new String[1];
        String[] postOrden = new String[1];
        String[] preOrden = new String[1];
        
        inOrden[0] = " ";
        postOrden[0] = " ";
        preOrden[0] = " ";
        
        b.obtenerN();
        b.posorden(b.n, postOrden);
        b.preorden(b.n, preOrden);
        b.inorden(b.n, inOrden);
        
        txtInorden.setText(inOrden[0]);
        txtPostorden.setText(postOrden[0]);
        txtPreorden.setText(preOrden[0]);
        
    }//GEN-LAST:event_btnRecorrerActionPerformed

