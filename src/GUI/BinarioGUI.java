    private void txtNumKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNumKeyTyped
        // TODO add your handling code here:
        char c = evt.getKeyChar();
        if (c < '0' || c > '9') {
            evt.consume();
        }
    }//GEN-LAST:event_txtNumKeyTyped

    private void btnReconstruirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReconstruirActionPerformed
        int lengthIn = this.txtInorden.getText().length();
        int lengthPre = this.txtPreorden.getText().length();
        int lengthPos = this.txtPostorden.getText().length();
        if(lengthIn!=0 && lengthPos!=0 && lengthPre!=0 && lengthIn==lengthPos && lengthIn==lengthPre){
            GraficaArbol ga = new GraficaArbol(this.txtPostorden.getText(), this.txtPreorden.getText(), this.txtInorden.getText());
            ga.setVisible(true);
        }
    }//GEN-LAST:event_btnReconstruirActionPerformed

    
